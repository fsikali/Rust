// Updating a Vector 
// To create a vector then add elements to it, we can use the push method

pub fn my_vector() { 
    let mut v = Vec::new(); 

    v.push(5); 
    v.push(6);
    v.push(7);
    v.push(8);
}

// As with any variable, if we want to be able to change its value, we need to make
// it mutable using the mut keyword
//  

pub fn my_vector() { 
    let mut v = Vec::new();
}

